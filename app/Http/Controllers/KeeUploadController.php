     * @return \Illuminate\Contracts\Foundation\Application|\Illuminate\Http\RedirectResponse|\Illuminate\Routing\Redirector|\Inertia\Response
     */
    public function store(Request $request)
    {

      $validation = $request->validate(
        [
          'kee_uploads' => 'file|mimes:xlsx,xlx,csv'
        ]);
      if ($validation) {
        $file = $request->file('kee_uploads');
        Excel::import(new KeesImport(), $file);

        return redirect('dashboard/manage/user');
      }
    }

